interface ServerEntry {
  name: string
  description: string
  path: string
  embedded: boolean
  productionEnabled?: boolean
  tools: any[]
}

async function loadAvailableServers(): Promise<ServerEntry[]> {
  // Dynamically import available-servers.ts from the platform repo
  // This requires running with tsx which handles TypeScript + path aliases
  const availableServersPath = path.join(
    PLATFORM_ROOT,
    'servers/agentic/mcp/src/servers/available-servers.ts'
  )

  try {
    const mod = await import(availableServersPath)
    return mod.AVAILABLE_SERVERS as ServerEntry[]
  } catch (e) {
    // If direct import fails (path aliases), fall back to loading tool files individually
    console.error(
      'Could not import available-servers.ts directly. Falling back to file-based loading.'
    )
    console.error(e)
    throw e
  }
}

async function loadToolsForServer(
  serverName: string
): Promise<any[] | null> {
  const toolsPath = path.join(SERVERS_DIR, serverName, 'tools.ts')
  if (!fs.existsSync(toolsPath)) {
    console.warn(`  Warning: No tools.ts found for ${serverName}`)
    return null
  }
  try {
    const mod = await import(toolsPath)
    return mod.TOOLS || null
  } catch (e) {
    console.warn(`  Warning: Failed to load tools for ${serverName}: ${e}`)
    return null
  }
}

/**
 * Folder names under external/ that are imported from available-servers.ts
 * under a different server `name` (e.g. microsoft-dynamics-crm → dynamics-crm).
 */
function getExternalDirsReferencedInRegistry(): Set<string> {
  const availableServersPath = path.join(
    PLATFORM_ROOT,
    'servers/agentic/mcp/src/servers/available-servers.ts'
  )
  if (!fs.existsSync(availableServersPath)) {
    return new Set()
  }
  const src = fs.readFileSync(availableServersPath, 'utf-8')
  const dirs = new Set<string>()
  const re =
    /from\s+['"]\.\/external\/([^/'"]+)\/tools\.js['"]/g
  let m: RegExpExecArray | null
  while ((m = re.exec(src)) !== null) {
    dirs.add(m[1])
  }
  return dirs
}

/**
 * Some branches may have `external/<name>/tools.ts` without a matching
 * `available-servers.ts` row (registry drift). Include those so docs and
 * docs.json stay aligned with on-disk tools.
 */
async function appendExternalServersMissingFromRegistry(
  registryServers: ServerEntry[]
): Promise<ServerEntry[]> {
  const byName = new Set(registryServers.map((s) => s.name))
  const referencedDirs = getExternalDirsReferencedInRegistry()
  const extra: ServerEntry[] = []

  const entries = fs.readdirSync(SERVERS_DIR, { withFileTypes: true })
  for (const ent of entries) {
    if (!ent.isDirectory()) continue
    const name = ent.name
    if (byName.has(name)) continue
    if (referencedDirs.has(name)) continue

    const tools = await loadToolsForServer(name)
    if (!tools || tools.length === 0) continue

    const rawDescription = `${name.replace(/-/g, ' ')} — application MCP tools`
    extra.push({
      name,
      description: getDescription(name, rawDescription),
      path: `/${name}`,
      embedded: false,
      productionEnabled: true,
      tools
    })
    byName.add(name)
  }

  if (extra.length > 0) {
    console.log(
      `Merged ${extra.length} external server(s) not in available-servers.ts: ${extra.map((s) => s.name).join(', ')}\n`
    )
  }

  return [...registryServers, ...extra]
}

// ============================================================================
// docs.json update
// ============================================================================

function updateDocsJson(serverNames: string[], selectiveServers?: string[]): void {
  const docsJson = JSON.parse(fs.readFileSync(DOCS_JSON_PATH, 'utf-8'))

  // Find the "Application MCP Servers" group in the navigation
  let appGroup: any = null
  function findGroup(obj: any): boolean {
    if (Array.isArray(obj)) {
      for (const item of obj) {
        if (findGroup(item)) return true
      }
    } else if (obj && typeof obj === 'object') {
      if (obj.group === 'Application MCP Servers') {
        appGroup = obj
        return true
      }
      for (const value of Object.values(obj)) {
        if (findGroup(value)) return true
      }
    }
    return false
  }
  findGroup(docsJson)

  if (!appGroup) {
    console.error('Could not find "Application MCP Servers" group in docs.json')
    process.exit(1)
  }

  if (selectiveServers) {
    // Add server pages if not already present, maintaining alphabetical order
    const existingPages: string[] = appGroup.pages || []
    const serverPages = existingPages.filter(
      (p) => p !== 'api-reference/mcp-servers/application/overview'
    )
    for (const name of selectiveServers) {
      const pagePath = `api-reference/mcp-servers/application/${name}`
      if (!serverPages.includes(pagePath)) {
        serverPages.push(pagePath)
      }
    }
    serverPages.sort()
    appGroup.pages = ['api-reference/mcp-servers/application/overview', ...serverPages]
  } else {
    // Replace all pages with overview + generated pages
    const pages = ['api-reference/mcp-servers/application/overview']
    for (const name of serverNames.sort()) {
      pages.push(`api-reference/mcp-servers/application/${name}`)
    }
    appGroup.pages = pages
  }

  fs.writeFileSync(DOCS_JSON_PATH, JSON.stringify(docsJson, null, 2) + '\n')
}

// ============================================================================
// Git-based change detection
// ============================================================================

function getChangedServers(baseRef: string): string[] {
  const externalPrefix = 'servers/agentic/mcp/src/servers/external/'

  const diffOutput = execSync(
    `git diff ${baseRef} --name-only -- "${externalPrefix}"`,
    { cwd: PLATFORM_ROOT, encoding: 'utf-8' }
  ).trim()

  const serverNames = new Set<string>()
  if (diffOutput) {
    for (const filePath of diffOutput.split('\n')) {
      const relative = filePath.replace(externalPrefix, '')
      const serverName = relative.split('/')[0]
      if (serverName) {
        serverNames.add(serverName)
      }
    }
  }

  return Array.from(serverNames).sort()
}

function getNewServersWithoutDocs(externalServers: ServerEntry[]): string[] {
  const newServers: string[] = []
  
  for (const server of externalServers) {
    const docPath = path.join(OUTPUT_DIR, `${server.name}.mdx`)
    if (!fs.existsSync(docPath)) {
      newServers.push(server.name)
    }
  }
  
  return newServers.sort()
}

// ============================================================================
// Main
// ============================================================================

async function main(): Promise<void> {
  const args = process.argv.slice(2)
  const serverFlagIdx = args.indexOf('--server')
  const singleServer =
    serverFlagIdx !== -1 ? args[serverFlagIdx + 1] : undefined
  const changedFlagIdx = args.indexOf('--changed')
  const changedBaseRef =
    changedFlagIdx !== -1 ? (args[changedFlagIdx + 1] || 'origin/main') : undefined

  if (singleServer && changedBaseRef) {
    console.error('Cannot use --server and --changed together.')
    process.exit(1)
  }

  console.log('=== Application MCP Server Documentation Generator ===\n')

  // Ensure output directory exists
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true })
  }

  if (singleServer && PARTITION_DOC_SLUG_SET.has(singleServer)) {
    const family = PARTITION_FAMILIES.find((f) => f.docSlug === singleServer)
    if (!family) {
      console.error(`Unknown parent/child doc slug: ${singleServer}`)
      process.exit(1)
    }
    const ok = await syncPartitionedFamilyDocPage(family)
    if (!ok) {
      console.error(
        `No child servers found for "${family.docSlug}" under platform mcp-server-definitions`
      )
      process.exit(1)
    }
    updateDocsJson([family.docSlug], [family.docSlug])
    console.log('\n=== Done ===')
    console.log(`  Output:  ${OUTPUT_DIR}/`)
    return
  }

  // Load server registry
  let servers: ServerEntry[]
  try {
    servers = await loadAvailableServers()
    servers = await appendExternalServersMissingFromRegistry(servers)
  } catch {
    // Fallback: scan external server directories and load tools directly
    console.log('Falling back to directory-based server discovery...\n')
    servers = await fallbackLoadServers()
  }

  // Filter to external, production-enabled servers
  const externalServers = servers.filter(
    (s) => s.embedded === false && s.productionEnabled !== false
  )

  console.log(
    `Found ${externalServers.length} production-enabled application servers\n`
  )

  // Determine which servers to generate
  let toGenerate: ServerEntry[]
  let selectiveServers: string[] | undefined

  if (changedBaseRef) {
    const changedNames = getChangedServers(changedBaseRef)
    const newServerNames = getNewServersWithoutDocs(externalServers)
    const touchedPartitionSlugs = PARTITION_FAMILIES.filter((f) =>
      mcpPartitionFamilyDefinitionsChanged(changedBaseRef, f.gitDiffPathPrefix)
    ).map((f) => f.docSlug)

    // Combine changed and new servers (servers without docs)
    let allTargetNames = Array.from(
      new Set([...changedNames, ...newServerNames, ...touchedPartitionSlugs])
    ).sort()

    if (allTargetNames.length === 0) {
      console.log(
        `No external server or parent/child MCP definition changes found compared to ${changedBaseRef}, and all servers have documentation.`
      )
      console.log('\n=== Done (nothing to generate) ===')
      return
    }
    
    if (changedNames.length > 0) {
      console.log(`Changed servers (vs ${changedBaseRef}): ${changedNames.join(', ')}`)
    }
    if (newServerNames.length > 0) {
      console.log(`New servers (without documentation): ${newServerNames.join(', ')}`)
    }
    if (touchedPartitionSlugs.length > 0) {
      console.log(
        `Partitioned MCP definitions changed (vs ${changedBaseRef}): ${touchedPartitionSlugs.join(', ')}`
      )
    }
    console.log('')

    toGenerate = externalServers.filter((s) => allTargetNames.includes(s.name))
    selectiveServers = allTargetNames
    const missing = allTargetNames.filter(
      (name) => !toGenerate.some((s) => s.name === name)
    )
    if (missing.length > 0) {
      console.warn(`  Warning: changed/new but not production-enabled or not found: ${missing.join(', ')}`)
    }
  } else if (singleServer) {
    toGenerate = externalServers.filter((s) => s.name === singleServer)
    selectiveServers = [singleServer]
    if (toGenerate.length === 0) {
      console.error(`Server "${singleServer}" not found or not production-enabled.`)
      console.error(
        'Available servers:',
        externalServers.map((s) => s.name).join(', ')
      )
      process.exit(1)
    }
  } else {
    toGenerate = externalServers
  }

  let totalTools = 0
  const generatedNames: string[] = []

  for (const server of toGenerate) {
    const tools = server.tools || (await loadToolsForServer(server.name))
    if (!tools || tools.length === 0) {
      console.warn(`  Skipping ${server.name}: no tools found`)
      continue
    }

    console.log(
      `  Generating ${server.name}.mdx (${tools.length} tools)...`
    )

    const mdx = generateServerPage(server.name, server.description, tools)
    const outputPath = path.join(OUTPUT_DIR, `${server.name}.mdx`)
    fs.writeFileSync(outputPath, mdx)

    totalTools += tools.length
    generatedNames.push(server.name)
  }

  for (const family of PARTITION_FAMILIES) {
    if (
      !shouldSyncPartitionedFamily(family, { singleServer, changedBaseRef })
    ) {
      continue
    }
    const written = await syncPartitionedFamilyDocPage(family)
    if (written && !generatedNames.includes(family.docSlug)) {
      generatedNames.push(family.docSlug)
    }
  }

  generatedNames.sort((a, b) => a.localeCompare(b))

  // Update docs.json
  if (generatedNames.length > 0) {
    if (selectiveServers) {
      updateDocsJson(generatedNames, selectiveServers)
      console.log(`\nUpdated docs.json with ${generatedNames.length} page(s)`)
    } else {
      updateDocsJson(generatedNames)
      console.log(`\nUpdated docs.json with ${generatedNames.length} pages`)
    }
  }

  console.log(`\n=== Done ===`)
  console.log(`  Servers: ${generatedNames.length}`)
  console.log(`  Tools:   ${totalTools}`)
  console.log(`  Output:  ${OUTPUT_DIR}/`)
}

// Fallback: scan directories if available-servers.ts can't be imported
async function fallbackLoadServers(): Promise<ServerEntry[]> {
  // Read available-servers.ts as text and extract server configs
  const availableServersPath = path.join(
    PLATFORM_ROOT,
    'servers/agentic/mcp/src/servers/available-servers.ts'
  )
  const source = fs.readFileSync(availableServersPath, 'utf-8')

  // Find all external server directories
  const dirs = fs.readdirSync(SERVERS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)

  const servers: ServerEntry[] = []

  for (const dirName of dirs) {
    // Extract server config from the source text
    const nameRegex = new RegExp(
      `name:\\s*'${dirName}'[\\s\\S]*?embedded:\\s*(true|false)`,
      'g'
    )
    const match = nameRegex.exec(source)
    if (!match || match[1] === 'true') continue // skip embedded or not found

    // Extract description
    const descRegex = new RegExp(
      `name:\\s*'${dirName}'[\\s\\S]*?description:[\\s\\S]*?['"\`]([^'"\`]+)['"\`]`
    )
    const descMatch = descRegex.exec(source)
    const description = descMatch ? descMatch[1] : `${dirName} API`

    // Check productionEnabled
    const prodRegex = new RegExp(
      `name:\\s*'${dirName}'[\\s\\S]*?productionEnabled:\\s*(true|false)`
    )
    const prodMatch = prodRegex.exec(source)
    const productionEnabled = prodMatch ? prodMatch[1] !== 'false' : true

    // Load tools
    const tools = await loadToolsForServer(dirName)
    if (!tools) continue

    servers.push({
      name: dirName,
      description,
      path: `/${dirName}`,
      embedded: false,
      productionEnabled,
      tools
    })
  }

  return servers
}

main().catch((e) => {
  console.error('Fatal error:', e)
  process.exit(1)
})
