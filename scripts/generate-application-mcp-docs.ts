type DefinitionPartition = {
  id: string
  description: string
  absPath: string
}

type DefinitionPartitionWithTools = DefinitionPartition & {
  tools: { name: string; description: string; inputSchema: any }[]
}

function parseMcpServerTs(src: string): { id: string; description: string } | null {
  const idM = src.match(/\bid:\s*'([^']*)'/)
  if (!idM) return null
  const descM = src.match(/\bdescription:\s*'([^']*)'/)
  return { id: idM[1], description: descM?.[1] ?? '' }
}

function discoverPartitionMetas(family: PartitionFamily): DefinitionPartition[] {
  if (!fs.existsSync(MCP_SERVER_DEFINITIONS_DIR)) return []
  const disc = family.discovery
  const out: DefinitionPartition[] = []

  if (disc.mode === 'flat-prefix') {
    for (const ent of fs.readdirSync(MCP_SERVER_DEFINITIONS_DIR, {
      withFileTypes: true
    })) {
      if (!ent.isDirectory() || !ent.name.startsWith(disc.prefix)) continue
      const absPath = path.join(MCP_SERVER_DEFINITIONS_DIR, ent.name)
      const serverTs = path.join(absPath, 'server.ts')
      const toolsDir = path.join(absPath, 'tools')
      if (!fs.existsSync(serverTs) || !fs.existsSync(toolsDir)) continue
      const meta = parseMcpServerTs(fs.readFileSync(serverTs, 'utf-8'))
      if (meta) out.push({ ...meta, absPath })
    }
  } else {
    const root = path.join(MCP_SERVER_DEFINITIONS_DIR, disc.rootRelativeDir)
    if (!fs.existsSync(root)) return []
    for (const ent of fs.readdirSync(root, { withFileTypes: true })) {
      if (!ent.isDirectory()) continue
      const absPath = path.join(root, ent.name)
      const serverTs = path.join(absPath, 'server.ts')
      const toolsDir = path.join(absPath, 'tools')
      if (!fs.existsSync(serverTs) || !fs.existsSync(toolsDir)) continue
      const meta = parseMcpServerTs(fs.readFileSync(serverTs, 'utf-8'))
      if (meta) out.push({ ...meta, absPath })
    }
  }

  return out.sort((a, b) => a.id.localeCompare(b.id))
}

async function loadDynamicToolDefinition(
  absPath: string
): Promise<{ name: string; description: string; inputSchema: any } | null> {
  try {
    const mod = (await import(pathToFileURL(absPath).href)) as { default?: any }
    const t = mod.default
    if (!t || typeof t.name !== 'string' || !t.inputSchema) return null
    return {
      name: t.name,
      description: typeof t.description === 'string' ? t.description : '',
      inputSchema: t.inputSchema
    }
  } catch (e) {
    console.warn(`  Warning: could not import tool ${absPath}: ${e}`)
    return null
  }
}

async function loadToolsForPartitionDir(
  partitionAbsBase: string
): Promise<{ name: string; description: string; inputSchema: any }[]> {
  const toolsDir = path.join(partitionAbsBase, 'tools')
  if (!fs.existsSync(toolsDir)) return []
  const files = fs
    .readdirSync(toolsDir)
    .filter((f) => f.endsWith('.ts'))
    .sort()
  const loaded: { name: string; description: string; inputSchema: any }[] = []
  for (const f of files) {
    const tool = await loadDynamicToolDefinition(path.join(toolsDir, f))
    if (tool) loaded.push(tool)
  }
  return loaded.sort((a, b) => a.name.localeCompare(b.name))
}

function jsonSchemaTypeLabel(prop: any): string {
  if (prop == null || typeof prop !== 'object') return 'any'
  if (prop.type === 'array') {
    return `${jsonSchemaTypeLabel(prop.items)}[]`
  }
  if (Array.isArray(prop.type)) return prop.type.join(' | ')
  if (typeof prop.type === 'string') return prop.type
  if (prop.properties) return 'object'
  if (prop.oneOf || prop.anyOf) return 'union'
  return 'object'
}

function extractParamsFromJsonSchema(inputSchema: {
  properties?: Record<string, any>
  required?: string[]
}): ParamInfo[] {
  const props = inputSchema.properties ?? {}
  const req = new Set(inputSchema.required ?? [])
  const params: ParamInfo[] = []
  for (const [name, schema] of Object.entries(props)) {
    if (name === 'PCID') continue
    const defVal =
      schema &&
      typeof schema === 'object' &&
      'default' in schema &&
      (schema as { default: unknown }).default !== undefined
        ? `\`${JSON.stringify((schema as { default: unknown }).default)}\``
        : '—'
    params.push({
      name,
      type: jsonSchemaTypeLabel(schema),
      required: req.has(name),
      defaultValue: defVal,
      description:
        schema &&
        typeof schema === 'object' &&
        typeof (schema as { description?: string }).description === 'string'
          ? (schema as { description: string }).description
          : ''
    })
  }
  return params
}

function generateToolSectionFromJsonSchema(tool: {
  name: string
  description: string
  inputSchema: any
}): string {
  const params = extractParamsFromJsonSchema(tool.inputSchema || {})

  let md = `## ${tool.name}\n\n`
  md += `${escapeMarkdown(tool.description)}\n\n`

  if (params.length > 0) {
    md += `**Parameters:**\n\n`
    md += `| Parameter | Type | Required | Default | Description |\n`
    md += `| --- | --- | --- | --- | --- |\n`
    for (const p of params) {
      md += `| \`${p.name}\` | ${p.type} | ${p.required ? 'Yes' : 'No'} | ${p.defaultValue} | ${escapeMarkdown(p.description)} |\n`
    }
    md += '\n'
  }

  md += `<Expandable title="inputSchema">\n\n`
  md += '```json\n'
  md += `${JSON.stringify(tool.inputSchema, null, 2)}\n`
  md += '```\n\n'
  md += `</Expandable>\n`

  return md
}

async function buildPartitionsWithToolsForFamily(
  family: PartitionFamily
): Promise<DefinitionPartitionWithTools[]> {
  const metas = discoverPartitionMetas(family)
  const filled: DefinitionPartitionWithTools[] = []
  for (const m of metas) {
    const tools = await loadToolsForPartitionDir(m.absPath)
    if (tools.length === 0) continue
    filled.push({ id: m.id, description: m.description, absPath: m.absPath, tools })
  }
  return filled
}

function mcpPartitionFamilyDefinitionsChanged(
  baseRef: string,
  gitDiffPathPrefix: string
): boolean {
  try {
    const diffOutput = execSync(`git diff ${baseRef} --name-only`, {
      cwd: PLATFORM_ROOT,
      encoding: 'utf-8'
    }).trim()
    if (!diffOutput) return false
    return diffOutput
      .split('\n')
      .some((line) => line.startsWith(gitDiffPathPrefix))
  } catch {
    return false
  }
}

async function syncPartitionedFamilyDocPage(
  family: PartitionFamily
): Promise<boolean> {
  const partitions = await buildPartitionsWithToolsForFamily(family)
  const outPath = path.join(OUTPUT_DIR, `${family.docSlug}.mdx`)

  if (partitions.length === 0) {
    if (fs.existsSync(outPath)) {
      fs.unlinkSync(outPath)
      console.log(
        `  Removed ${family.docSlug}.mdx (no partitions for family "${family.docSlug}")`
      )
    } else {
      console.log(
        `  No partitions for family "${family.docSlug}" — skipping ${family.docSlug}.mdx`
      )
    }
    return false
  }

  const title = getDisplayName(family.docSlug)
  const description = getDescription(
    family.docSlug,
    family.fallbackDescription
  )

  let md = `---\n`
  md += `title: "${title}"\n`
  md += `sidebarTitle: "${family.docSlug}"\n`
  md += `description: "${description.replace(/"/g, '\\"')}"\n`
  md += `---\n\n`
  md += `${GENERATED_HEADER}\n\n`
  md += `**PinkConnect service:** \`${family.pinkConnectService}\` | **Type:** Application (parent with child servers) | **PCID required:** Yes\n\n`
  md += `${family.introParagraph}\n\n`

  md += `## Child servers\n\n`
  md += `| Child server | Server path | Tools | Description |\n`
  md += `| --- | --- | --- | --- |\n`
  for (const p of partitions) {
    md += `| [${escapeMarkdown(p.id)}](#${p.id}) | \`/${p.id}\` | ${p.tools.length} | ${escapeMarkdown(p.description)} |\n`
  }
  md += '\n'

  for (const p of partitions) {
    md += `\n---\n\n## ${p.id}\n\n`
    md += `${escapeMarkdown(p.description)}\n\n`
    md += `**Server path:** \`/${p.id}\` | **Type:** Application | **PCID required:** Yes\n\n`

    md += `### Tools\n\n`
    md += `| Tool | Description |\n`
    md += `| --- | --- |\n`
    for (const t of p.tools) {
      md += `| [\`${t.name}\`](#${t.name}) | ${escapeMarkdown(t.description)} |\n`
    }

    for (const t of p.tools) {
      md += '\n---\n\n'
      md += generateToolSectionFromJsonSchema(t)
    }
  }

  fs.writeFileSync(outPath, md)
  const totalTools = partitions.reduce((acc, part) => acc + part.tools.length, 0)
  console.log(
    `  Generating ${family.docSlug}.mdx (${partitions.length} partitions, ${totalTools} tools)...`
  )
  return true
}

function shouldSyncPartitionedFamily(
  family: PartitionFamily,
  opts: { singleServer?: string; changedBaseRef?: string }
): boolean {
  if (opts.singleServer) {
    return opts.singleServer === family.docSlug
  }
  if (opts.changedBaseRef) {
    return mcpPartitionFamilyDefinitionsChanged(
      opts.changedBaseRef,
      family.gitDiffPathPrefix
    )
  }
  return true
}

