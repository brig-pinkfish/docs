interface ServerEntry {
  name: string
  label?: string
  description: string
  path: string
  embedded: boolean
  availableToAgents?: boolean
  availableToUsers?: boolean
  productionEnabled?: boolean
  tools: any[]
}

async function loadAvailableServers(): Promise<ServerEntry[]> {
  const availableServersPath = path.join(
    PLATFORM_ROOT,
    'servers/agentic/mcp/src/servers/available-servers.ts'
  )

  try {
    const mod = await import(availableServersPath)
    return mod.AVAILABLE_SERVERS as ServerEntry[]
  } catch (e) {
    console.error(
      'Could not import available-servers.ts directly. Ensure you are running from platform/servers/agentic/mcp/'
    )
    console.error(e)
    throw e
  }
}

function filterEmbeddedServers(servers: ServerEntry[]): ServerEntry[] {
  return servers.filter((s) => {
    if (!s.embedded) return false
    if (EXCLUDED_SERVERS.has(s.name)) return false
    // Include servers available to users OR agents (public-facing)
    if (s.availableToUsers === false && s.availableToAgents === false) return false
    return true
  })
}

// ============================================================================
// docs.json update
// ============================================================================

function updateDocsJson(serverSlugs: string[], selectiveSlugs?: string[], slugToLabel?: Map<string, string>): void {
  /** Sort page paths by their display label (falling back to slug) */
  const sortByLabel = (pages: string[]) => {
    return pages.sort((a, b) => {
      const slugA = a.split('/').pop() || a
      const slugB = b.split('/').pop() || b
      const labelA = (slugToLabel?.get(slugA) || slugA).toLowerCase()
      const labelB = (slugToLabel?.get(slugB) || slugB).toLowerCase()
      return labelA.localeCompare(labelB)
    })
  }
  const docsJson = JSON.parse(fs.readFileSync(DOCS_JSON_PATH, 'utf-8'))

  // Find the "Embedded MCP Servers" group in the navigation
  let embeddedGroup: any = null
  function findGroup(obj: any): boolean {
    if (Array.isArray(obj)) {
      for (const item of obj) {
        if (findGroup(item)) return true
      }
    } else if (obj && typeof obj === 'object') {
      if (obj.group === 'Embedded MCP Servers') {
        embeddedGroup = obj
        return true
      }
      for (const value of Object.values(obj)) {
        if (findGroup(value)) return true
      }
    }
    return false
  }
  findGroup(docsJson)

  if (!embeddedGroup) {
    console.error('Could not find "Embedded MCP Servers" group in docs.json')
    process.exit(1)
  }

  if (selectiveSlugs) {
    // Add server pages if not already present, maintaining alphabetical order
    const existingPages: string[] = embeddedGroup.pages || []
    // Preserve overview and manual pages
    const staticPages = existingPages.filter(
      (p: string) =>
        p === 'api-reference/mcp-servers/embedded/overview' ||
        [...MANUAL_PAGES].some((mp) => p === `api-reference/mcp-servers/embedded/${mp}`)
    )
    const serverPages = existingPages.filter(
      (p: string) =>
        p !== 'api-reference/mcp-servers/embedded/overview' &&
        ![...MANUAL_PAGES].some((mp) => p === `api-reference/mcp-servers/embedded/${mp}`)
    )
    for (const slug of selectiveSlugs) {
      const pagePath = `api-reference/mcp-servers/embedded/${slug}`
      if (!serverPages.includes(pagePath)) {
        serverPages.push(pagePath)
      }
    }
    embeddedGroup.pages = [
      'api-reference/mcp-servers/embedded/overview',
      ...sortByLabel(insertManualPages(serverPages))
    ]
  } else {
    // Full replacement
    const serverPages = serverSlugs.map(
      (slug) => `api-reference/mcp-servers/embedded/${slug}`
    )
    embeddedGroup.pages = [
      'api-reference/mcp-servers/embedded/overview',
      ...sortByLabel(insertManualPages(serverPages))
    ]
  }

  fs.writeFileSync(DOCS_JSON_PATH, JSON.stringify(docsJson, null, 2) + '\n')
}

/** Insert manual pages into the sorted server pages list at the correct alphabetical position */
function insertManualPages(serverPages: string[]): string[] {
  const manualEntries = [...MANUAL_PAGES]
    .map((mp) => `api-reference/mcp-servers/embedded/${mp}`)
  const combined = [...serverPages]
  for (const entry of manualEntries) {
    if (!combined.includes(entry)) {
      combined.push(entry)
    }
  }
  return combined.sort()
}

// ============================================================================
// Git-based change detection
// ============================================================================

function getChangedServers(baseRef: string): string[] {
  const embeddedPrefix = 'servers/agentic/mcp/src/servers/embedded/'
  const sandboxPrefix = 'servers/agentic/mcp/src/groups/sandbox/'

  let diffOutput: string
  try {
    diffOutput = execSync(
      `git diff ${baseRef} --name-only -- "${embeddedPrefix}" "${sandboxPrefix}"`,
      { cwd: PLATFORM_ROOT, encoding: 'utf-8' }
    ).trim()
  } catch {
    return []
  }

  const serverNames = new Set<string>()
  if (diffOutput) {
    for (const filePath of diffOutput.split('\n')) {
      if (filePath.startsWith(embeddedPrefix)) {
        const relative = filePath.replace(embeddedPrefix, '')
        const dirName = relative.split('/')[0]
        if (dirName && dirName !== 'shared') {
          // Reverse-map code dir to server name
          const serverName = codeDirToServerName(dirName)
          if (serverName) serverNames.add(serverName)
        }
      }
      if (filePath.startsWith(sandboxPrefix)) {
        serverNames.add('code-execution')
      }
    }
  }

  return Array.from(serverNames).sort()
}

/** Reverse lookup: given embedded/ subdirectory name, find the server name */
function codeDirToServerName(dirName: string): string | null {
  // Check reverse of NAME_TO_CODE_DIR
  for (const [name, dir] of Object.entries(NAME_TO_CODE_DIR)) {
    if (dir === dirName) return name
  }
  // Default: directory name is the server name
  return dirName
}

function getNewServersWithoutDocs(embeddedServers: ServerEntry[]): string[] {
  const newServers: string[] = []

  for (const server of embeddedServers) {
    const docSlug = getDocSlug(server.name)
    const docPath = path.join(OUTPUT_DIR, `${docSlug}.mdx`)
    if (!fs.existsSync(docPath)) {
      newServers.push(server.name)
    }
  }

  return newServers.sort()
}

