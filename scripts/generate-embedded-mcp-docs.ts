async function main(): Promise<void> {
  const args = process.argv.slice(2)
  const serverFlagIdx = args.indexOf('--server')
  const singleServer =
    serverFlagIdx !== -1 ? args[serverFlagIdx + 1] : undefined
  const changedFlagIdx = args.indexOf('--changed')
  const changedBaseRef =
    changedFlagIdx !== -1 ? (args[changedFlagIdx + 1] || 'origin/main') : undefined

  if (singleServer && changedBaseRef) {
    console.error('Cannot use --server and --changed together.')
    process.exit(1)
  }

  console.log('=== Embedded MCP Server Documentation Generator ===\n')

  // Ensure output directory exists
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true })
  }

  // Load server registry
  let servers: ServerEntry[]
  try {
    servers = await loadAvailableServers()
  } catch {
    process.exit(1)
  }

  // Filter to embedded, non-excluded servers
  const embeddedServers = filterEmbeddedServers(servers)

  console.log(
    `Found ${embeddedServers.length} documentable embedded servers\n`
  )

  // Determine which servers to generate
  let toGenerate: ServerEntry[]
  let selectiveSlugs: string[] | undefined

  if (changedBaseRef) {
    const changedNames = getChangedServers(changedBaseRef)
    const newServerNames = getNewServersWithoutDocs(embeddedServers)

    const allTargetNames = Array.from(
      new Set([...changedNames, ...newServerNames])
    ).sort()

    if (allTargetNames.length === 0) {
      console.log(
        `No embedded server changes found compared to ${changedBaseRef}, and all servers have documentation.`
      )
      console.log('\n=== Done (nothing to generate) ===')
      return
    }

    if (changedNames.length > 0) {
      console.log(`Changed servers (vs ${changedBaseRef}): ${changedNames.join(', ')}`)
    }
    if (newServerNames.length > 0) {
      console.log(`New servers (without documentation): ${newServerNames.join(', ')}`)
    }
    console.log('')

    toGenerate = embeddedServers.filter((s) => allTargetNames.includes(s.name))
    selectiveSlugs = allTargetNames.map(getDocSlug)
    const missing = allTargetNames.filter(
      (name) => !toGenerate.some((s) => s.name === name)
    )
    if (missing.length > 0) {
      console.warn(`  Warning: changed/new but excluded or not found: ${missing.join(', ')}`)
    }
  } else if (singleServer) {
    // Allow specifying by either server name or doc slug
    toGenerate = embeddedServers.filter(
      (s) => s.name === singleServer || getDocSlug(s.name) === singleServer
    )
    if (toGenerate.length === 0) {
      console.error(`Server "${singleServer}" not found among documentable embedded servers.`)
      console.error(
        'Available:',
        embeddedServers.map((s) => s.name).join(', ')
      )
      process.exit(1)
    }
    selectiveSlugs = toGenerate.map((s) => getDocSlug(s.name))
  } else {
    toGenerate = embeddedServers
  }

  // Build slug-to-label map from all embedded servers (not just toGenerate)
  const slugToLabel = new Map<string, string>()
  for (const server of embeddedServers) {
    const slug = getDocSlug(server.name)
    if (server.label) {
      slugToLabel.set(slug, server.label)
    }
  }

  let totalTools = 0
  const generatedSlugs: string[] = []

  for (const server of toGenerate) {
    const tools = server.tools
    if (!tools || tools.length === 0) {
      console.warn(`  Skipping ${server.name}: no tools found`)
      continue
    }

    const docSlug = getDocSlug(server.name)
    console.log(
      `  Generating ${docSlug}.mdx (${tools.length} tools)...`
    )

    const mdx = generateServerPage(server.name, server.description, tools, server.label)
    const outputPath = path.join(OUTPUT_DIR, `${docSlug}.mdx`)
    fs.writeFileSync(outputPath, mdx)

    totalTools += tools.length
    generatedSlugs.push(docSlug)
  }

  // Update docs.json
  if (generatedSlugs.length > 0) {
    if (selectiveSlugs) {
      updateDocsJson(generatedSlugs, selectiveSlugs, slugToLabel)
      console.log(`\nUpdated docs.json with ${generatedSlugs.length} page(s)`)
    } else {
      updateDocsJson(generatedSlugs, undefined, slugToLabel)
      console.log(`\nUpdated docs.json with ${generatedSlugs.length} pages`)
    }
  }

  console.log(`\n=== Done ===`)
  console.log(`  Servers: ${generatedSlugs.length}`)
  console.log(`  Tools:   ${totalTools}`)
  console.log(`  Output:  ${OUTPUT_DIR}/`)
}

main().catch((e) => {
  console.error('Fatal error:', e)
  process.exit(1)
})
